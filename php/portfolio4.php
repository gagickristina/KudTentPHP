                            <p>Nešto što se vrlo teško briše iz srca jeste upravo ljubav prema igri i pesmi. Upravo naši rekreativci dokazuju da ne postoji doba u životu čoveka koje bi mu onemogućilo da istu tu ljubav neguje. Zahvaljujući inicijativi članova Umetničkog saveta Centra, svim ljubiteljima narodne igre i pesme omogućeni su adekvatni uslovi za rekreativnim bavljenjem folklora, te je tako u septembru mesecu 2017. godine oformljena Rekreativna folklorna grupa Centra za očuvanje tradicije i kulture ''Termoelektrane Nikola Tesla'' Obrenovac.

