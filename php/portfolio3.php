                            <p>Prva mešovita pevačka grupa kulturno-umetničkog društva ,,Termoelektrane Nikola Tesla'' Obrenovac je osnovana u martu 2001. godine, a dužnost rukovodioca ove mešovite pevačke grupe do 2014. godine je obavljao etnomuzikolog Biljana Konjević.

                            Pevačka grupa kulturno-umetničkog društva ,,Termoelektrane Nikola Tesla'' Obrenovac je obnovljena u decembru 2015. godine, pod nazivom ,,Đurđe'', na inicijativu Sanje Nikolić, istaknutog člana Mešovitog hora Ministarstva odbrane i Vojske Srbije i hora ,,Obrenovačke devojke''. Ovu pevačku grupu čine kako članovi koji su pevali u raznim horovima, etno grupama i kamernim sastavima, tako i članovi Izvođačkog folklornog ansambla. Njihovo dugogodišnje iskustvo rezultiralo je da se već nakon nekoliko proba sačini bogat repertoar sa preko četrdeset pesama, kojima su se predstavile na svom prvom samostalnom koncertu pod nazivom „Pesmom kroz Srbiju” (31. januar 2016. godine). Nakon dvogodišnjeg napornog rada i usavršavanja članovi pevačke grupe ,,Đurđe'' su na radost svih Obrenovčana priredili koncert tradicionalne pesme i muzike pod nazivom ,,Putem tradicije'', na praznik Sretenje (15. februar 2018. godine). Članovi ove pevačke grupe su nebrojano puta uzimali učešće na svim važnijim kulturnim manifestacijama u Obrenovcu i drugim gradovima Srbije. U avgustu 2017. godine uzeli su učešće na Međunarodnom festivalu amaterskog stvaralaštva ,,Balkan Folk Fest'' u Varni (Bugarska), osvojivši najviše priznanje - Zlatnu plaketu Festivala. Na repertoaru pevačke grupe „Đurđe” trenutno se nalazi preko osamdeset pesama iz svih krajeva Srbije i ostalih zemalja regiona, a najzastupljenije su pesme sa juga Srbije, tj. Kosova i Metohije.</p>
